
#include<stdio.h>
#include<stdbool.h>
int main(){
    // Integer Variable
    int bananaPrice = 20;
    printf("bananaPrice = %d\n",bananaPrice);

    // Float Variable
    float chocolatePrice = 2.5;
    printf("chocolatePrice = %0.1f\n",chocolatePrice);

    // Character Variable
    char name = 'A';
    printf("Character = %c\n",name);

    // Boolean Variable
    bool a = true;
    bool b = false;
    printf("True = %d\n",a);
    printf("False = %d\n",b);

    return 0;
}